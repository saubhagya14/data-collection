package com.ies.data.collection.binding;

public class Kid {

	private String kidName;
	private String kidAge;
	private String kidSsn;
	
	public String getKidName() {
		return kidName;
	}
	public void setKidName(String kidName) {
		this.kidName = kidName;
	}
	public String getKidAge() {
		return kidAge;
	}
	public void setKidAge(String kidAge) {
		this.kidAge = kidAge;
	}
	public String getKidSsn() {
		return kidSsn;
	}
	public void setKidSsn(String kidSsn) {
		this.kidSsn = kidSsn;
	}
	
	
}
