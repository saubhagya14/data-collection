package com.ies.data.collection.binding;

public class IncomeDetails {

	private Integer caseNumber;
	private Integer userId;
	private Double sallaryIncome;
	private Double rentIncome;
	private Double propertyIncome;
	
	public Integer getCaseNumber() {
		return caseNumber;
	}
	public void setCaseNumber(Integer caseNumber) {
		this.caseNumber = caseNumber;
	}
	public Double getSallaryIncome() {
		return sallaryIncome;
	}
	public void setSallaryIncome(Double sallaryIncome) {
		this.sallaryIncome = sallaryIncome;
	}
	public Double getRentIncome() {
		return rentIncome;
	}
	public void setRentIncome(Double rentIncome) {
		this.rentIncome = rentIncome;
	}
	public Double getPropertyIncome() {
		return propertyIncome;
	}
	public void setPropertyIncome(Double propertyIncome) {
		this.propertyIncome = propertyIncome;
	}
	public Integer getUserId() {
		return userId;
	}
	public void setUserId(Integer userId) {
		this.userId = userId;
	}
	
	
}
